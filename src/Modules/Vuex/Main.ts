//---------------------------------------------------------------------------
// Imports
//---------------------------------------------------------------------------
import { Action, Module, Mutation, VuexModule } from "vuex-module-decorators";


//---------------------------------------------------------------------------
/**
 * The main store module.
 */
//---------------------------------------------------------------------------
@Module
export default class Main extends VuexModule
{

    //---------------------------------------------------------------------------
    // State Properties
    //---------------------------------------------------------------------------
    public Message: string = "";


    //---------------------------------------------------------------------------
    /**
     * UpdateMessage
     */
    //---------------------------------------------------------------------------
    @Action public UpdateMessage(message: string): void
    {
        this.context.commit("SetMessage", message);
    }


    //---------------------------------------------------------------------------
    /**
     * SetMessage
     */
    //---------------------------------------------------------------------------
    @Mutation protected SetMessage(message: string): void
    {
        this.Message = message;
    }

}